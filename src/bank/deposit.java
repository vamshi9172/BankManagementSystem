        // TODO add your handling code here:
        if(deposittf.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"please enter correct values");
        }
        else
        {
            try{
           Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/bankdb","root","Vamshi@31");
           Statement s=c.createStatement();
           String pinn=pintf.getText();
           ResultSet rs=s.executeQuery("select * from balancetable where pin='"+pinn+"'");
           if(rs.next())
           {
               String pin=rs.getString("pin");
               double balance=Double.parseDouble(rs.getString("balance"));
               balance+=Double.parseDouble(deposittf.getText());
               s.executeUpdate("update balancetable set balance='"+Double.toString(balance)+"' where pin='"+pin+"'");
               JOptionPane.showMessageDialog(null,"deposited succesfully");
               new transaction().setVisible(true);
               setVisible(false);
           }
           else
               JOptionPane.showMessageDialog(null,"enter correct pin value");
            }
        catch(SQLException e){
            System.out.println(e);
        }
        }
